A comprehensive website for the "Empowering Futures" initiative, which supports teenage mothers in Sierra Leone to return to education and break the cycle of early marriage and poverty.

##  Project Overview

Empowering Futures is a non-profit initiative founded by Josephine Duba Kanu that addresses the critical issue of teenage pregnancy and forced early marriage in Sierra Leone. The organization provides comprehensive support systems to enable young mothers to continue their education while caring for their children.

### Key Features:
- **Educational Support**: Flexible schooling options and scholarship programs
- **Childcare Services**: On-site nursery facilities for young mothers
- **Life Skills Training**: Financial literacy, parenting skills, and career guidance
- **Health & Wellness**: Maternal health support and counseling services

